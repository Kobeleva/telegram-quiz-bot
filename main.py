    btn3 = types.InlineKeyboardButton("3. Музыка", callback_data="cat_3") 
    btn4 = types.InlineKeyboardButton("4. Спорт", callback_data="cat_4")
    btn5 = types.InlineKeyboardButton("5. История", callback_data="cat_5")
    btn6 = types.InlineKeyboardButton("6. Литература", callback_data="cat_6")
    markup.add(btn1, btn2, btn3, btn4, btn5, btn6)
