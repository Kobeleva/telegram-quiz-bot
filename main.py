def start_cmd(message):
    user_id = message.chat.id
    if user_id in sessions:
        del sessions[user_id]
        
    greet = f"Привет, {message.from_user.first_name}!\nВыбери категорию викторины:"
    bot.send_message(user_id, greet, reply_markup=create_main_menu())

# 5. ОБРАБОТКА НАЖАТИЙ НА КНОПКИ
@bot.callback_query_handler(func=lambda call: True)
def handle_buttons(call):
    user_id = call.message.chat.id
    data = call.data

    # Игрок кликнул на категорию (cat_1, cat_2, cat_3 ...)
    if data.startswith("cat_"):
        cat_id = int(data.split("_")[1])  # Вот здесь [1] обязателен!
        
        # Создаем чистую сессию (Сброс старого прогресса)
        sessions[user_id] = {"cat": cat_id, "score": 0, "q_index": 0}
        
        bot.send_message(user_id, "🚀 Игра началась!")
        send_question(user_id)
        
    # Игрок кликнул на вариант ответа
    elif data.startswith("ans_"):
        if user_id not in sessions:
            bot.send_message(user_id, "⚠️ Игра уже неактивна. Нажми /start")
            bot.answer_callback_query(call.id)
            return
            
        user_answer = data[4:]
        session = sessions[user_id]
        current_q = questions[session["cat"]][session["q_index"]]
        
        # Проверяем правильность
        if user_answer == current_q["correct"]:
            session["score"] += 10
            bot.send_message(user_id, "✅ Правильно!")
        else:
            bot.send_message(user_id, f"❌ Неверно. Правильный ответ: {current_q['correct']}")
            
        # Переходим к следующему вопросу
        session["q_index"] += 1
        send_question(user_id)

    # Убираем вечную загрузку на кнопке
    bot.answer_callback_query(call.id)

